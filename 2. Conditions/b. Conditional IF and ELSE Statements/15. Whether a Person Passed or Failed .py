# a program to determine whether a person has passed or failed based on their marks using conditional statements.

marks = float(input("\nEnter your marks: "))

if marks >= 50:
    print("You have passed.")
else:
    print("You have failed.")
    
# Explanation:
# The program prompts the user to input their marks. It then uses a conditional statement (if, else) to check if the marks are greater than or equal to 50. If the condition is true, it prints that the person has passed; otherwise, it prints that the person has failed.