# A Program to update the value of an existing key.

student = {
    "name": "Hasher",
    "age": 20
}

student["age"] = 21

print(student)

# Explanation:
# The program creates a dictionary and then searches for the key "age". Since the key already exists, Python replaces its old value (20) with the new value (21). The key remains the same, but its associated value changes. The updated dictionary is then printed.

# Real-Life Use:
# Updating dictionary values is used when editing customer information, updating stock quantities, modifying account details, or changing application settings.