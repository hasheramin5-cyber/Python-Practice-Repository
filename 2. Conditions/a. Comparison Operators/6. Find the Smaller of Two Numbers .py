# Find the Smaller of Two Numbers

num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

if num1 < num2:
    print(f"{num1} is smaller")
else:
    print(f"{num2} is smaller")
    
# Explanation:
# In this code snippet, we take two numbers as input from the user and convert them to floats. We then compare the two numbers using an if statement. If the first number is smaller than the second number, we print that the first number is smaller. Otherwise, we print that the second number is smaller. This code helps in determining which of the two entered numbers is smaller.