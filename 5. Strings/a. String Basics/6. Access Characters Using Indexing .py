# A Program to access characters of a string using indexing.

text = input("\nEnter a string: ")

print("First Character :", text[0])
print("Second Character:", text[1])
print("Third Character :", text[2])

# Explanation:
# The program demonstrates string indexing by accessing individual characters using their index positions. Since indexing starts from 0, the first character is at index 0, the second at index 1, and the third at index 2. But if the entered string is less than 3 characters it will generate an error.