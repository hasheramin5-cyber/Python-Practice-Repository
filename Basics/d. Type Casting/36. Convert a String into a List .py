# Convert a String into a List in Python.

text = "Python"

converted_text = list(text)

print(converted_text)
print(type(converted_text))

# Explaination:
# In this code snippet, we have a variable 'text' that holds a string value "Python". We then use the list() function to convert this string into a list of its characters and store it in the variable 'converted_text'. Finally, we print the converted list and its type to confirm that the conversion was successful. The output will show that 'converted_text' is now of type <class 'list'>.