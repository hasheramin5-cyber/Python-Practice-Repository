# A Program to print all elements of a list.

colors = ["Red", "Green", "Blue", "Yellow"]
fruits = ["Apple", "Mango", "Banana", "Orange"]

print("\nList No.1 : ", colors)
print("List No.2 : ", fruits)

# Explanation:
# The program stores multiple color names inside a list and displays the entire list using the print() function. When a list is printed, Python displays all of its elements enclosed in square brackets.