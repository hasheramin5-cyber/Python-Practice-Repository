# Check if a Person is Eligible for a Driving License.

age = int(input("Enter your age: "))

if age >= 18:
    print("Eligible for a driving license.")
else:
    print("Not eligible for a driving license.")
    
# Explanation:
# In this code snippet, we take the age of a person as input from the user and convert it to an integer. We then check if the age is greater than or equal to 18 using an if statement. If the condition is true, we print "Eligible for a driving license.". Otherwise, we print "Not eligible for a driving license.". This code helps in determining whether a person is eligible for a driving license based on their age.