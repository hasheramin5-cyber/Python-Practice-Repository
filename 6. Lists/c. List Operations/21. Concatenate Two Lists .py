# A Program to concatenate two lists.

list1 = [10, 20, 30]
list2 = [40, 50, 60]

print("\nBefore Combine List1 is", list1,"and List2 is", list2)

combined_list = list1 + list2

print("Combined List is:", combined_list)

# Explanation:
# The program combines two lists using the + operator. A new list is created containing all the elements of the first list followed by all the elements of the second list. The original lists remain unchanged.