# Using Type Casting to convert a string to an integer in Python.

number = "100"

converted_number = int(number)

print(converted_number)
print(type(converted_number))

# Explaination:
# In this code snippet, we have a variable 'number' that holds a string value "100". We then use the int() function to convert this string into an integer and store it in the variable 'converted_number'. Finally, we print the converted number and its type to confirm that the conversion was successful. The output will show that 'converted_number' is now of type <class 'int'>.