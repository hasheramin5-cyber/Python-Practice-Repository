# Convert an Integer into a String in Python.

number = 500

converted_number = str(number)

print(converted_number)
print(type(converted_number))

# Explaination:
# In this code snippet, we have a variable 'number' that holds an integer value 500. We then use the str() function to convert this integer into a string and store it in the variable 'converted_number'. Finally, we print the converted number and its type to confirm that the conversion was successful. The output will show that 'converted_number' is now of type <class 'str'>.