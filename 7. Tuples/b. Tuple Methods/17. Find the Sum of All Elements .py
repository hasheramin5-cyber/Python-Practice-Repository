# A Program to calculate the sum of all Elements in a Tuple.

numbers = (10, 20, 30, 40, 50)

print(f"\nTuple: {numbers}")
total = sum(numbers)

print("Sum:", total)

# Explanation:
# The program calculates the total of all numeric values in the tuple using the sum() function. This operation is frequently used in financial software, reports, data analysis, and many applications where numerical values need to be combined into a single result.