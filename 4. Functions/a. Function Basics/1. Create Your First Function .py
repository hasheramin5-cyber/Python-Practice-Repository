# A Program to create your first function.

def greet():
    print("\nHello, World!")

# Calling the function
greet()

# Explanation:
# The program defines a function named 'greet' using the 'def' keyword. The function contains a simple print statement that displays "Hello, World!". The function does not run automatically when it is created. It executes only when it is called using its name followed by parentheses.