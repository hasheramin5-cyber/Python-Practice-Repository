# A Program to calculate the sum of all elements in a list.

numbers = [10, 20, 30, 40, 50]

print("\nList:", numbers)
total = sum(numbers)

print(f"Sum of List: {numbers} is {total}.")

# Explanation:
# The program calculates the sum of all numeric elements in the list using the sum() function. The function adds each element and returns the total.