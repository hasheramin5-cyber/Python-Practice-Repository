# A Program to convert Celsius to Fahrenheit using a function.

def celsius_to_fahrenheit(celsius):
    fahrenheit = (celsius * 9 / 5) + 32
    print("Temperature =", fahrenheit, "°F")

celsius_to_fahrenheit(25)

# Explanation:
# The program defines a function that converts a temperature from Celsius to Fahrenheit using the standard conversion formula. The Celsius value is passed as an argument, and the converted Fahrenheit temperature is displayed.