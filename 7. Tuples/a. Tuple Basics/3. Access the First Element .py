# A Program to Access the first Element of a Tuple.

numbers = (10, 20, 30, 40, 50)

print(f"\nTuple: {numbers}")
print("First Element:", numbers[0])

# Explanation:
# The program accesses the first element using index 0. Python uses zero-based indexing, which means counting begins from zero instead of one. Understanding indexing is essential because it is used throughout Python for tuples, lists, strings, and many other data structures.