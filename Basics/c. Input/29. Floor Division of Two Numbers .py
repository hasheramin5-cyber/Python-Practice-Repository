# Taking Floor Division of Two Numbers from user and print the results.

num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

print("Floor Division =", num1 // num2)

# Explaination: In this code, we are taking two numbers as input from the user and then performing floor division operation on those two numbers. Finally, we are printing the result of the floor division operation.
