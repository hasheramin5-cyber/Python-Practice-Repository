# Check if Two Numbers are Equal.

num1 = float(input("\nEnter first number: "))
num2 = float(input("Enter second number: "))

if num1 == num2:
    print("Both numbers are equal.")
else:
    print("Numbers are not equal.")
    
# Explanation:
# In this code snippet, we take two numbers as input from the user and convert them to floats. We then compare the two numbers using an if statement. If the first number is equal to the second number, we print that both numbers are equal. Otherwise, we print that the numbers are not equal. This code helps in determining whether the two entered numbers are equal or not.