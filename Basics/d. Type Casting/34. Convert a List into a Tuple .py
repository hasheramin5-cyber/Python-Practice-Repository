# Convert a List into a Tuple in Python

numbers = [10, 20, 30, 40]

converted_numbers = tuple(numbers)

print(converted_numbers)
print(type(converted_numbers))

# Explaination:
# In this code snippet, we have a variable 'numbers' that holds a list of integers [10, 20, 30, 40]. We then use the tuple() function to convert this list into a tuple and store it in the variable 'converted_numbers'. Finally, we print the converted tuple and its type to confirm that the conversion was successful. The output will show that 'converted_numbers' is now of type <class 'tuple'>.