# A Program to print the square of a number using a function.

def square(number):
    print("Square =", number ** 2)

square(7)

# Explanation:
# The program defines a function that accepts a number as a parameter and calculates its square using the exponent operator (**). Whenever the function is called with a different value, it calculates and displays the square of that value, making the function reusable for different inputs.