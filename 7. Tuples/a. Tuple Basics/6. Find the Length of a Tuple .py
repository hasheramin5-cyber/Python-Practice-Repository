# A Program to find the Length of a Tuple.

animals = ("Cat", "Dog", "Horse", "Rabbit", "Lion")

print(f"\nTuple: {animals}")

print(f"Length of Tuple is {len(animals)}.")

# Explanation:
# The program uses the len() function to count the total number of elements in the tuple. Knowing the length of a collection is useful when validating user input, creating loops, or determining whether a tuple contains the expected amount of data.