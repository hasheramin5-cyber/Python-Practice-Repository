# A Program to access the Last Element of a Tuple.

numbers = (10, 20, 30, 40, 50)

print(f"\nTuple: {numbers}")
print("Last Element:", numbers[-1])

# Explanation:
# The program accesses the last element using negative indexing. In Python, an index of -1 always refers to the final element of a sequence. Negative indexing provides a simple and efficient way to access elements starting from the end without knowing the total length of the tuple.