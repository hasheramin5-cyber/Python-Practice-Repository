# A Program to print numbers from 1 to 10 using a while loop.

number = 1

while number <= 10:
    print(number)
    number += 1

# Explanation:
# The program uses a while loop to print numbers from 1 to 10. It starts by assigning the value 1 to the variable 'number'. The loop continues to execute as long as the value of 'number' is less than or equal to 10. During each iteration, the current value is printed, and then the variable is increased by 1. When the value becomes 11, the condition becomes False, and the loop stops.