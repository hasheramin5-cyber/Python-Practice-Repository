# A Program to find the maximum of two numbers using a function.

def maximum(number1, number2):
    if number1 > number2:
        print("Maximum =", number1)
    else:
        print("Maximum =", number2)

maximum(35, 42)

# Explanation:
# The program defines a function that compares two numbers and displays the larger one. It uses an if-else statement to determine which number is greater. This demonstrates how functions can be combined with conditional statements to solve decision-making problems.