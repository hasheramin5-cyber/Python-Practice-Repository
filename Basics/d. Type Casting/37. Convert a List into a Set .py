# Convert a List into a Set in Python.

numbers = [10, 20, 30, 20, 10]

converted_numbers = set(numbers)

print(converted_numbers)
print(type(converted_numbers))

# Explaination:
# In this code snippet, we have a variable 'numbers' that holds a list of integers [10, 20, 30, 20, 10]. We then use the set() function to convert this list into a set and store it in the variable 'converted_numbers'. Finally, we print the converted set and its type to confirm that the conversion was successful. The output will show that 'converted_numbers' is now of type <class 'set'> and will contain only unique elements from the original list.