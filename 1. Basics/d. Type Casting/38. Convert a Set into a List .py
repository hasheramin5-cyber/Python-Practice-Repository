# Convert a Set into a List in Python.

numbers = {10, 20, 30}

converted_numbers = list(numbers)

print(converted_numbers)
print(type(converted_numbers))

# Explaination:
# In this code snippet, we have a variable 'numbers' that holds a set of integers {10, 20, 30}. We then use the list() function to convert this set into a list and store it in the variable 'converted_numbers'. Finally, we print the converted list and its type to confirm that the conversion was successful. The output will show that 'converted_numbers' is now of type <class 'list'>.