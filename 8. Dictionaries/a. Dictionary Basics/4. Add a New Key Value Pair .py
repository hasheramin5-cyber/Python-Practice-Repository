# A Program to add a new key-value pair to a dictionary.

student = {
    "name": "Hasher",
    "age": 20
}

student["course"] = "Python"

print(student)

# Explanation:
# The program first creates a dictionary with two key-value pairs. A new key named "course" is then assigned the value "Python". Since this key does not already exist, Python adds it to the dictionary. Finally, the updated dictionary is printed, showing all three key-value pairs.

# Real-Life Use:
# Adding new keys is common when users provide additional information while filling out registration forms or updating their profiles.