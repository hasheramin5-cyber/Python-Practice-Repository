# A Program to find the Maximum Element in a Tuple.

numbers = (45, 12, 89, 67, 23)

print(f"\nTuple: {numbers}")
print("Maximum Element:", max(numbers))

# Explanation:
# The program uses Python's built-in max() function to find the largest value stored in the tuple. The function compares all elements and returns the highest one. In future exercises, you'll implement this logic manually using loops to understand how searching algorithms work.