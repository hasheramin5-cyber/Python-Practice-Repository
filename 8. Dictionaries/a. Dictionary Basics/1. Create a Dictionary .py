# A Program to create a Dictionary.

student = {
    "name": "Hasher",
    "age": 20,
    "course": "Python"
}

print(student)

# Explanation:
# The program begins by creating a dictionary named 'student'. A dictionary stores data as key-value pairs, where each key has a corresponding value. Here, the key "name" stores the value "Hasher", the key "age" stores 20, and the key "course" stores "Python". Once the dictionary is created, the print() function displays all of its contents. Dictionaries are useful when data has labels because you can access information using meaningful keys instead of remembering index positions.

# Real-Life Use: Dictionaries are commonly used to store user profiles, employee records, product information, API responses, and configuration settings.