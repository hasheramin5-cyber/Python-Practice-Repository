# A Program to iterate through a list using a for loop.

subjects = ["Python", "Mathematics", "Physics", "English"]

for subject in subjects:
    print(subject)

# Explanation:
# The program uses a for loop to visit each element of the list one by one. During every iteration, the current element is stored in the variable 'subject' and then displayed. This is the most common way to process all elements of a list in Python.