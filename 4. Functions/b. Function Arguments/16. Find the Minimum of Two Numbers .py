# A Program to find the minimum of two numbers using a function.

def minimum(number1, number2):
    if number1 < number2:
        print("Minimum =", number1)
    else:
        print("Minimum =", number2)

minimum(35, 42)

# Explanation:
# The program defines a function that compares two numbers and displays the smaller one. It uses an if-else statement to determine which value is less. The same function can be reused with different numbers whenever needed.