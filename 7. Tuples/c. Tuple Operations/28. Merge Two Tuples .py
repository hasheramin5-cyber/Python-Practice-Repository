# A Program to merge two tuples.

tuple1 = (10, 20, 30)
tuple2 = (40, 50, 60)

print(f"\nTuple1: {tuple1}\nTuple2: {tuple2}")

merged_tuple = tuple1 + tuple2

print("Merged Tuple:", merged_tuple)

# Explanation:
# The program combines two tuples using the addition (+) operator. Rather than modifying either tuple, Python creates a new tuple containing the elements of both tuples in order. This operation is commonly used when combining related collections of fixed data.