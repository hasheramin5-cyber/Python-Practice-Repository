# A Program to iterate through all dictionary keys.

student = {
    "name": "Hasher",
    "age": 20,
    "course": "Python"
}

for key in student:
    print(key)

# Explanation:
# The program creates a dictionary and starts a for loop. By default, a for loop iterates through the dictionary's keys. During each iteration, one key is assigned to the variable 'key', which is then printed. The loop continues until every key has been visited, after which the program ends.

# Real-Life Use:
# Iterating through keys is useful when displaying available settings, field names, categories, or menu options stored in a dictionary.