# A Program to Create a Tuple.

fruits = ("Apple", "Banana", "Mango", "Orange")

print("\nTuple:", fruits)

# Explanation:
# The program creates a tuple containing four fruit names. A tuple is an ordered collection of items enclosed in parentheses (). Unlike lists, tuples are immutable, meaning their elements cannot be changed after creation. Tuples are commonly used to store fixed data that should remain constant throughout a program, such as coordinates, dates, or configuration values.