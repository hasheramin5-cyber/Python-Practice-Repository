# A Program to print a Dictionary.

car = {
    "brand": "Toyota",
    "model": "Corolla",
    "year": 2024
}

print("\nCar Information:")
print(car)

# Explanation:
# The program first creates a dictionary named 'car' containing information about a vehicle. Each piece of information is stored as a key-value pair. The print() function then displays the complete dictionary. Python prints every key along with its corresponding value inside curly braces. Printing dictionaries is useful while testing programs because it allows developers to verify that the stored data is correct.

# Real-Life Use:
# Printing dictionaries helps developers debug applications and inspect data received from databases, APIs, or configuration files.