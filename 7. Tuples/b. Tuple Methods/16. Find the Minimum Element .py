# A Program to find the Minimum Element in a Tuple.

numbers = (45, 12, 89, 67, 23)

print(f"\nTuple: {numbers}")
print("Minimum Element:", min(numbers))

# Explanation:
# The program uses the min() function to find the smallest value in the tuple. Python compares every element and returns the lowest value. Later, you'll practice solving the same problem without relying on built-in functions to strengthen your programming logic.