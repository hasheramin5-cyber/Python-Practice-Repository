# A Program to calculate the average of all elements in a list.

numbers = [10, 20, 30, 40, 50]

print(f"\nList: {numbers}")
average = sum(numbers) / len(numbers)

print("Average of List is:", average)

# Explanation:
# The program calculates the average by dividing the sum of all elements by the total number of elements in the list. It uses both the sum() and len() functions.