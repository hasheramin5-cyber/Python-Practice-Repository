# Check Whether a Number is Divisible by 5.

number = int(input("Enter a number: "))

if number % 5 == 0:
    print("Divisible by 5.")
else:
    print("Not divisible by 5.")
    
# Explanation:
# In this code snippet, we take a number as input from the user and convert it to an integer. We then check if the number is divisible by 5 using the modulus operator (%). If the remainder when the number is divided by 5 is 0, we print "Divisible by 5.". Otherwise, we print "Not divisible by 5.". This code helps in determining whether the entered number is divisible by 5 or not.