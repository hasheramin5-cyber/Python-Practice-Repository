# Check if a Person is Eligible to Vote.

age = int(input("Enter your age: "))

if age >= 18:
    print("Eligible to vote.")
else:
    print("Not eligible to vote.")
    
# Explanation:
# In this code snippet, we take the age of a person as input from the user and convert it to an integer. We then check if the age is greater than or equal to 18 using an if statement. If the condition is true, we print "Eligible to vote.". Otherwise, we print "Not eligible to vote.". This code helps in determining whether a person is eligible to vote based on their age.