# A Program to remove all items from a Dictionary.

student = {
    "name": "Hasher",
    "age": 20,
    "course": "Python"
}

student.clear()

print(student)

# Explanation:
# The program creates a dictionary and then calls the clear() method. Python removes every key-value pair from the dictionary, leaving it empty. The dictionary itself still exists, but it no longer contains any data. Printing it shows an empty pair of curly braces {}.

# Real-Life Use:
# The clear() method is useful when resetting forms, clearing cached data, or preparing a dictionary to store new information.