# A Program to print numbers from 10 to 1 using a while loop.

number = 10

while number >= 1:
    print(number)
    number -= 1

# Explanation:
# The program prints numbers in reverse order from 10 to 1 using a while loop. It begins with the value 10 and decreases it by 1 after every iteration. The loop continues until the value becomes less than 1, producing the numbers in descending order.