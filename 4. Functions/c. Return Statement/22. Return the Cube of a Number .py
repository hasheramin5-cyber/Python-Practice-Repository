# A Program to return the cube of a number using a function.

def cube(number):
    return number ** 3

result = cube(4)
print("Cube =", result)

# Explanation:
# The program defines a function that returns the cube of a given number. The return statement sends the calculated value back to the calling code, where it is stored in a variable and displayed. Returning values makes functions more flexible and reusable.