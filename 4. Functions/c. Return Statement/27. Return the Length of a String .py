# A Program to return the length of a string using a function.

def string_length(text):
    return len(text)

result = string_length("Python")
print("Length =", result)

# Explanation:
# The program defines a function that returns the length of a string using Python's built-in len() function. Instead of printing the value directly, the function returns it so it can be stored, displayed, or used elsewhere in the program.