# A Program to create a function with two parameters.

def add(number1, number2):
    print("Sum =", number1 + number2)

add(15, 20)

# Explanation:
# The program defines a function that accepts two parameters. When the function is called, two values are passed as arguments and stored in the parameters. The function adds both values together and displays the result. Functions with multiple parameters allow us to work with more than one piece of data at a time.