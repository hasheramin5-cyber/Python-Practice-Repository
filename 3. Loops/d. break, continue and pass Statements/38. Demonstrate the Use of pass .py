# A Program to demonstrate the use of the pass statement.

for number in range(1, 6):
    if number == 3:
        pass
    print(number)

# Explanation:
# The program demonstrates the use of the pass statement. When the value becomes 3, the pass statement does nothing and simply acts as a placeholder. The loop continues executing normally, showing that pass is useful when a statement is required syntactically but no action needs to be performed.