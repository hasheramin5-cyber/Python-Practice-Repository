# A Program to print all elements of a tuple.

colors = ("Red", "Green", "Blue", "Yellow")

print("\nTuple:", colors)

# Explanation:
# The program prints the complete tuple. Since a tuple is a collection, Python displays all of its elements together. Printing tuples is useful for verifying stored data during development and debugging. In later programs, you'll learn different ways to display individual elements and iterate through tuples.