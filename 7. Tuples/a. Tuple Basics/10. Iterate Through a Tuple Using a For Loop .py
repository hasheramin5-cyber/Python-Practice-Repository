# A Program to Iterate through a Tuple using a for loop.

countries = ("Pakistan", "Japan", "Canada", "Germany")

print("\nCountries:")

for country in countries:
    print(country)

# Explanation:
# The program uses a for loop to visit each element of the tuple one by one. Iteration is one of the most important programming concepts because it allows you to process every item in a collection efficiently. In future sections, you'll combine loops with nested tuples, dictionaries, files, and other data structures to build larger and more practical applications.