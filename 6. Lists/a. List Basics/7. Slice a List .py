# A Program to slice a list.

numbers = [10, 20, 30, 40, 50, 60, 70]

print("\nOriginal List is:", numbers)
print("Sliced List is:", numbers[1:5])

# Explanation:
# The program demonstrates list slicing. The slice [1:5] extracts elements starting from index 1 up to, but not including, index 5. Slicing creates a new list without modifying the original list.