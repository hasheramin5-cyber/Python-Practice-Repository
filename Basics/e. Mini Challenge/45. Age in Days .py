# Age in Days Calculator in Python.

age = int(input("Enter your age in years: "))
days = age * 365

print(f"You are approximately {days} days old.")

# Explaination:
# In this code snippet, we take the user's age in years as input and calculate the approximate number of days they have lived by multiplying the age by 365. We then print the result in a formatted string, showing the user's age in days. Note that this calculation does not account for leap years or the exact date of birth, so it provides an approximate value.