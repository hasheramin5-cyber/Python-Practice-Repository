# A Program to remove all elements from a list using the clear() method.

students = ["Ali", "Ahmed", "Sara", "Ayesha"]

print("\nBefore Update List is:", students)
students.clear()

print("After Update List is:", students)

# Explanation:
# The program removes all elements from the list using the clear() method. After clearing the list, it becomes an empty list while the variable itself still exists.