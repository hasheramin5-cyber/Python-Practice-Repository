# A Program to iterate through a nested list.

students = [
    ["Ali", 20],
    ["Sara", 21],
    ["Ahmed", 19]
]

for student in students:
    print(student)

# Explanation:
# The program uses a for loop to iterate through each inner list. During every iteration, one complete student record is retrieved and displayed. Iterating through nested lists is an essential skill because it allows you to process rows of data efficiently before moving on to accessing individual values within those rows.