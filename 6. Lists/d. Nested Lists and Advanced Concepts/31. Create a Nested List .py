# A Program to create a Nested List.

students = [
    ["Ali", 20],
    ["Sara", 21],
    ["Ahmed", 19]
]

print(f"\nNested List:\n{students}")

# Explanation:
# The program creates a nested list, which is a list containing other lists as its elements. Each inner list represents a student's record containing their name and age. Nested lists are commonly used to organize related data into rows and columns, similar to tables in spreadsheets or databases. Understanding nested lists is important because they form the basis for working with matrices, datasets, and structured information in Python.