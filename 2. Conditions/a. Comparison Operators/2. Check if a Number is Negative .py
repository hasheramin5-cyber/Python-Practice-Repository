# Check if a Number is Negative

number = float(input("Enter a number: "))

if number < 0:
    print(number, "is a Negative Number")
else:
    print(number, "is not a Negative Number")


# Explanation:
# In this code snippet, we take a number as input from the user and convert it to a float. We then check if the number is less than 0 using an if statement. If the condition is true, we print "Negative Number". This code helps in determining whether the entered number is negative or not.