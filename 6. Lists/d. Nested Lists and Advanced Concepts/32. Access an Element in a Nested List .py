# A Program to Access an Element from a Nested List.

students = [
    ["Ali", 20],
    ["Sara", 21],
    ["Ahmed", 19]
]

print("Student Name:", students[1][0])

# Explanation:
# The program demonstrates how to access elements inside a nested list. The first index selects the inner list, while the second index selects an element from that inner list. For example, students[1][0] means: go to the second student's record, then retrieve the first value from that record. This indexing technique is widely used when working with tables and multidimensional data.