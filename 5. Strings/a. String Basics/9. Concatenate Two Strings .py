# A Program to concatenate two strings.

first_name = input("\nEnter your first name: ")
last_name = input("Enter your last name: ")

full_name = first_name + " " + last_name

print("Full Name =", full_name)

# Explanation:
# The program combines two strings using the + operator. This process is called string concatenation. A space is added between the first name and last name to create a properly formatted full name.