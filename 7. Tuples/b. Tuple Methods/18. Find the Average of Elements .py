# A Program to calculate the average of the elements in a Tuple.

numbers = (10, 20, 30, 40, 50)

print(f"\nTuple: {numbers}")
average = sum(numbers) / len(numbers)

print("Average:", average)

# Explanation:
# The program calculates the average by dividing the sum of all elements by the total number of elements in the tuple. It combines the sum() and len() functions to perform the calculation. Understanding averages is important because they are widely used in statistics, grading systems, and data analysis.