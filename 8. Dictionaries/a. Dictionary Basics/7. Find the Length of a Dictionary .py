# A Program to find the length of a dictionary.

student = {
    "name": "Hasher",
    "age": 20,
    "course": "Python"
}

print("Number of Items:", len(student))

# Explanation:
# The program creates a dictionary containing three key-value pairs. The len() function counts how many key-value pairs are stored in the dictionary and returns that number. The result is then printed. Unlike lists, the length of a dictionary represents the total number of keys it contains.

# Real-Life Use:
# Counting dictionary items is useful for validating data, checking completed forms, and analyzing records in software applications.