# A Program to print each digit of a number on a new line.

number = input("Enter a number: ")

for digit in number:
    print(digit)
 
# Explanation:
# The program asks the user to enter a number and stores it as a string. It then uses a loop to access each character of the string one by one and prints every digit on a separate line. Treating the number as a string makes it easy to access each digit individually without performing mathematical operations.