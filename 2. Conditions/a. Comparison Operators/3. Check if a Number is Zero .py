# Check if a Number is Zero

number = float(input("Enter a number: "))

if number == 0:
    print("Number is Zero.")
else:
    print("Number is not Zero.")

# Explanation:
# In this code snippet, we take a number as input from the user and convert it to a float. We then check if the number is equal to 0 using an if statement. If the condition is true, we print "Number is Zero". This code helps in determining whether the entered number is zero or not.