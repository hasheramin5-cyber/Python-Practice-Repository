# A Program to Slice a Tuple.

numbers = (10, 20, 30, 40, 50, 60)

print("\nOriginal Tuple:", numbers)
print("Sliced Tuple:", numbers[1:5])

# Explanation:
# The program extracts a portion of the tuple using slicing. The slice starts from index 1 and ends before index 5. Slicing creates a new tuple containing only the selected elements while leaving the original tuple unchanged. This technique is frequently used when working with subsets of data.