# Using Type Casting to convert an integer to a float in Python.

number = 100

converted_number = float(number)

print(converted_number)
print(type(converted_number))

# Explaination:
# In this code snippet, we have a variable 'number' that holds an integer value 100. We then use the float() function to convert this integer into a float and store it in the variable 'converted_number'. Finally, we print the converted number and its type to confirm that the conversion was successful. The output will show that 'converted_number' is now of type <class 'float'>.