# A Program to return the square of a number using a function.

def square(number):
    return number ** 2

result = square(6)
print("Square =", result)

# Explanation:
# The program defines a function that calculates the square of a number and returns the result using the return statement. Unlike print(), the return statement sends the calculated value back to the place where the function was called. The returned value is stored in the variable 'result' and then displayed.