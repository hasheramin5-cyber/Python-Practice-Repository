# A Program to convert a Tuple into a List.

colors = ("Red", "Green", "Blue")

colors_list = list(colors)

print("\nOriginal Tuple:", colors)
print("Converted List:", colors_list)

# Explanation:
# The program converts a tuple into a list using the list() function. Since tuples are immutable and lists are mutable, this conversion is useful when you need to modify, add, or remove elements. Converting between data types is a common practice in Python programming.