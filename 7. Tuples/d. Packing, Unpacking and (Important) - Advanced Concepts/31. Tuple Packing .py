# A Program to demonstrate Tuple Packing.

student = "Hasher", 20, "Python"

print("Packed Tuple:", student)

# Explanation:
# The program demonstrates tuple packing. Instead of creating a tuple using parentheses, Python automatically packs multiple values into a single tuple when they are separated by commas. Here, the values "Hasher", 20, and "Python" are packed together into one tuple stored in the variable 'student'. Tuple packing makes code shorter and more readable. It is commonly used when returning multiple values from functions and working with fixed collections of related information.